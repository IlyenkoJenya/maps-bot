def _parse_variants(text: str) -> tuple[str, str, str]:
    parts = re.split(r"===\w+===", text)
    parts = [p.strip() for p in parts if p.strip()]

    if len(parts) >= 3:
        return parts[0], parts[1], parts[2]
    if len(parts) == 2:
        return parts[0], parts[1], parts[1]
    if len(parts) == 1:
        return parts[0], parts[0], parts[0]
    return "Хороший сервис, рекомендую!", "Хороший сервис, рекомендую!", "Хороший сервис, рекомендую!"
