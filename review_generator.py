_SERVICE_OVERRIDE = [None, None, None, None, "обслуживание"]

_CURRENCY_FORMATS = ["тг", "тнг", "тенге", ""]  # "" = вообще без валюты


def _format_cost(amount: str) -> str:
    if not str(amount).strip():
        return ""
    suffix = random.choice(_CURRENCY_FORMATS)
    return f"{amount} {suffix}".strip() if suffix else str(amount)


def _extract_model(product_type: str) -> str:
    if not product_type:
        return ""
    name = product_type.split(",")[0].strip().rstrip("|").strip()
    generic = {"монтаж системы", "монтаж", "ремонт", "диагностика", "протечка"}
    if not name or name.lower() in generic:
        return ""
    return name


def _build_context(record: dict) -> str:
    parts = []
    work_type = record.get("work_type", "").strip()
    product_type = record.get("product_type", "").strip()
    model = _extract_model(product_type)
    address = record.get("address", "").strip()
    cartridge_type = record.get("cartridge_type", "").strip()
    comment = record.get("comment", "").strip()
    maintenance_cost = record.get("maintenance_cost", "")
    product_cost = record.get("product_cost", "")

    if work_type:
        parts.append(f"Тип работы: {work_type}")
    if model:
        parts.append(f"Модель фильтра: {model}")
    elif product_type:
        parts.append(f"Тип продукта: {product_type}")
    if cartridge_type:
        parts.append(f"Картриджи: {cartridge_type}")
    if address:
        m = re.search(r"([\w-]+ский район|улица\s+[\w\s-]+|проспект\s+[\w\s-]+)", address, re.I)
        if m:
            parts.append(f"Район: {m.group(0).strip()}")
    if maintenance_cost:
        parts.append(f"Стоимость обслуживания: {_format_cost(maintenance_cost)}")
    if product_cost:
        parts.append(f"Стоимость товара: {_format_cost(product_cost)}")
    if comment:
        parts.append(f"Комментарий мастера: {comment}")

    return "\n".join(parts) if parts else "Плановое обслуживание фильтра Aquasoft"


def _build_mention_instruction(record: dict) -> str:
    """Randomly decide whether and what to mention from the record."""
    model = _extract_model(record.get("product_type", ""))
    work_type = record.get("work_type", "").strip()
    cartridge_type = record.get("cartridge_type", "").strip()

    hints = []
    # ~50% chance to mention filter model if available
    if model and random.random() < 0.5:
        hints.append(f"название фильтра ({model})")
    # ~50% chance to mention service/work type
    if work_type and random.random() < 0.5:
        hints.append(f"тип услуги ({work_type})")
    # ~60% chance to mention cartridges if available
    if cartridge_type and random.random() < 0.6:
        hints.append(f"картриджи ({cartridge_type})")

    if hints:
        return "Можешь естественно упомянуть в тексте: " + ", ".join(hints) + ". Не обязательно всё сразу."
    return "Не нужно обязательно называть конкретные модели или картриджи."


